package ebook.web.model;

public class Book {

	public String title;

	public String getTitle() {
		return title;
	}

	public String url;

	public String getUrl() {
		return url;
	}

	public String description;

	public String getDescription() {
		return description;
	}

	public String image;

	public String getImage() {
		return image;
	}

	public boolean hasImage;

	public boolean isHasImage() {
		return hasImage;
	}

}
